<script>
document.addEventListener('DOMContentLoaded', function() {
    const track = document.getElementById('carousel-track');
    const prevBtn = document.getElementById('prev-btn');
    const nextBtn = document.getElementById('next-btn');
    const indicators = document.querySelectorAll('.about-carousel-indicator');
    let currentIndex = 0;
    const totalSlides = 5;
    
    function updateCarousel() {
        track.style.transform = `translateX(-${currentIndex * 100}%)`;
        
        // Update indicators
        indicators.forEach((indicator, index) => {
            if (index === currentIndex) {
                indicator.classList.remove('bg-gray-300');
                indicator.classList.add('bg-blue-600');
            } else {
                indicator.classList.remove('bg-blue-600');
                indicator.classList.add('bg-gray-300');
            }
        });
        
        // Reset all quiz states
        const allQuizOptions = document.querySelectorAll('.quiz-option');
        allQuizOptions.forEach(option => {
            option.disabled = false;
            option.style.cursor = 'pointer';
            option.classList.remove('border-green-500', 'bg-green-50', 'border-red-500', 'bg-red-50');
            
            const mark = option.querySelector('.answer-mark');
            mark.textContent = '';
            mark.classList.add('hidden');
            mark.classList.remove('text-green-600', 'text-red-600');
        });
    }
    
    prevBtn.addEventListener('click', function() {
        currentIndex = (currentIndex - 1 + totalSlides) % totalSlides;
        updateCarousel();
    });
    
    nextBtn.addEventListener('click', function() {
        currentIndex = (currentIndex + 1) % totalSlides;
        updateCarousel();
    });
    
    indicators.forEach(indicator => {
        indicator.addEventListener('click', function() {
            currentIndex = parseInt(this.dataset.index);
            updateCarousel();
        });
    });
    
    // Quiz option click handler
    const quizOptions = document.querySelectorAll('.quiz-option');
    quizOptions.forEach(option => {
        option.addEventListener('click', function() {
            const parent = this.closest('.quiz-options');
            const allOptions = parent.querySelectorAll('.quiz-option');
            
            // Disable all options in this question
            allOptions.forEach(opt => {
                opt.disabled = true;
                opt.style.cursor = 'not-allowed';
            });
            
            const isCorrect = this.dataset.correct === 'true';
            const mark = this.querySelector('.answer-mark');
            
            if (isCorrect) {
                // Show correct mark (○)
                mark.textContent = '○';
                mark.classList.remove('hidden');
                mark.classList.add('text-green-600');
                this.classList.add('border-green-500', 'bg-green-50');
            } else {
                // Show incorrect mark (×)
                mark.textContent = '×';
                mark.classList.remove('hidden');
                mark.classList.add('text-red-600');
                this.classList.add('border-red-500', 'bg-red-50');
                
                // Also show the correct answer
                allOptions.forEach(opt => {
                    if (opt.dataset.correct === 'true') {
                        const correctMark = opt.querySelector('.answer-mark');
                        correctMark.textContent = '○';
                        correctMark.classList.remove('hidden');
                        correctMark.classList.add('text-green-600');
                        opt.classList.add('border-green-500', 'bg-green-50');
                    }
                });
            }
        });
    });
});
